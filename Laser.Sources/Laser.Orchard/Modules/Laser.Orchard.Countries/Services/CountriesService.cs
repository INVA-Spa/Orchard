        private IEnumerable<CountriesRecord> GetNazioni() {
            return _nazioniItemRepository.Fetch(x => x.Id > 0);
        }
        private IEnumerable<LaserCountriesZoneRecord> GetZone() {
            return _CountriesZoneRecord.Fetch(x => x.Id > 0);
        }
        private IEnumerable<LaserLinkedCountriesZoneRecord> GetLinkedZone(int idzone) {
            return _linkedCountriesZoneRecord.Fetch(x => x.LaserCountriesZoneRecord_Id == idzone);
        }
        public List<LaserCountriesZoneRecord> GetAllZone() {
            return _CountriesZoneRecord.Fetch(x => x.Id > 0).ToList();
        }
        public List<CountriesRecord> GetAllNazione(int idzone=-1) {
            if (idzone == -1) {
                return _nazioniItemRepository.Fetch(x => x.Id > 0).OrderBy(o => o.Des_local).ToList();
            } else {
                var elenco = _linkedCountriesZoneRecord.Fetch(x => x.LaserCountriesZoneRecord_Id == idzone);
                List<int> elencoid = new List<int>();
                foreach (var el in elenco)
                    elencoid.Add(el.CountriesRecord_Id);
                return _nazioniItemRepository.Fetch(x => elencoid.Contains(x.Id)).OrderBy(o => o.Des_local).ToList();
            }
        }
        public CountriesRecord GetNazione(int IdNazione) {
            return _nazioniItemRepository.Fetch(x => x.Id == IdNazione).SingleOrDefault();
        }

        public List<LaserCountriesZoneRecord> GetZone(int IdNazione){
            var ZoneList=_linkedCountriesZoneRecord.Fetch(x=>x.CountriesRecord_Id==IdNazione).ToList();
            List<int> listID = new List<int>();
            foreach (var zonex in ZoneList) {
                listID.Add(zonex.LaserCountriesZoneRecord_Id);
            }
            return _CountriesZoneRecord.Fetch(x => listID.Contains(x.Id)).ToList();
        }

