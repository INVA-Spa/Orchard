$(function () {
    var dd = $(".primary");

    $(".summary").click(function (e) {
        if ($(this).closest(".summary").find('.primary').stop().is(':visible')) {
            $(this).closest(".summary").find('.primary').stop().hide();
        } else {
            $(this).closest(".summary").find('.primary').stop().slideToggle(300);
        }
    });
});